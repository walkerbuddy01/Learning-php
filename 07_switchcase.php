<?php

//switch case 


$age = 90;

switch ($age) {
    case 0:
        echo "DO THIS";
        break;
    case 90:
        echo "DO NOTHING";
        break;
    case 70:
        echo "DO THIS ON 70";
        break;
    case 50:
        echo "DO THIS ON 50";
        break;
    default:
        echo "THIS HAPPENS EVERY TIME";

}


?>